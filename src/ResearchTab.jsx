// DEEP RESEARCH
// ═══════════════════════════════════════════════════════════════════
function DeepResearch({ conn, assign, goSettings }) {
  const [topic,       setTopic]       = useState("");
  const [rounds,      setRounds]      = useState(3);
  const [saveBrain,   setSaveBrain]   = useState(true);
  const [running,     setRunning]     = useState(false);
  const [results,     setResults]     = useState([]);   // {roleId, content, summary}
  const [activeRole,  setActiveRole]  = useState(null);
  const [activeRound, setActiveRound] = useState(0);
  const [liveText,    setLiveText]    = useState("");
  const [done,        setDone]        = useState(false);
  const [happening,   setHappening]   = useState(null);
  const outputRef = useRef(null);

  useEffect(()=>{ outputRef.current?.scrollTo(0,outputRef.current.scrollHeight); },[liveText,results]);

  const callModel = async (prompt) => {
    if (!conn || !assign?.model) throw new Error("No model configured");
    const endpoint = conn.type==="local"
      ? `${conn.url}/v1/chat/completions`
      : `${conn.url}/chat/completions`;

    const res = await fetch(endpoint, {
      method:"POST",
      headers:{ "Content-Type":"application/json", ...(conn.apiKey?{"Authorization":`Bearer ${conn.apiKey}`}:{}) },
      body: JSON.stringify({
        model:assign.model,
        messages:[{ role:"user", content:prompt }],
        stream:true, temperature:0.8,
      }),
    });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);

    const reader  = res.body.getReader();
    const decoder = new TextDecoder();
    let full = "";
    while (true) {
      const {done,value} = await reader.read();
      if (done) break;
      const lines = decoder.decode(value).split("\n").filter(l=>l.startsWith("data: "));
      for (const line of lines) {
        const data = line.slice(6);
        if (data==="[DONE]") continue;
        try {
          const delta = JSON.parse(data).choices?.[0]?.delta?.content||"";
          full += delta;
          setLiveText(full);
          setHappening(h=>({...h,tokens:(h?.tokens||0)+1}));
        } catch(e){}
      }
    }
    return full;
  };

  const runResearch = async () => {
    if (!topic.trim() || !assign?.model) return;
    setRunning(true); setResults([]); setDone(false); setLiveText(""); setActiveRound(0);

    const history = [];
    // Determine which roles to use based on rounds
    const roleSequence = [];
    for (let r=0; r<rounds; r++) {
      roleSequence.push(COUNCIL_ROLES[Math.min(r, COUNCIL_ROLES.length-1)]);
    }
    // Always end with archivist if saveBrain is on
    if (saveBrain && roleSequence[roleSequence.length-1].id !== "archivist") {
      roleSequence.push(COUNCIL_ROLES[3]); // archivist
    }

    try {
      for (let i=0; i<roleSequence.length; i++) {
        const role = roleSequence[i];
        setActiveRole(role.id);
        setActiveRound(i+1);
        setLiveText("");
        setHappening({ phase:`Round ${i+1} — ${role.name}`, model:assign.model, local:conn?.type==="local", tokens:0 });

        const prompt  = role.prompt(topic, i+1, history, false);
        const content = await callModel(prompt);
        const summary = content.slice(0,200)+"...";

        history.push({ role:role.name, summary:content });
        setResults(prev=>[...prev,{ roleId:role.id, content, summary }]);
        setLiveText("");

        // save archivist output to brain
        if (role.id==="archivist" && saveBrain) {
          const entry = { id:Date.now(), text:content, topic, source:"deep-research", savedAt:new Date().toISOString() };
          const existing = JSON.parse(localStorage.getItem("oni_brain")||"[]");
          localStorage.setItem("oni_brain", JSON.stringify([...existing, entry]));
        }
      }
      setDone(true);
      setHappening(h=>({...h,phase:"COMPLETE"}));
    } catch(e) {
      setLiveText(`\n✗ Error: ${e.message}`);
    }
    setRunning(false); setActiveRole(null);
  };

  const exportPDF = async () => {
    try {
      await generatePDF(topic, results, assign?.model||"Unknown");
    } catch(e) {
      alert("PDF export failed: "+e.message+"\n\nMake sure jspdf is installed: npm install jspdf");
    }
  };

  return (
    <div className="dr-layout">

      {/* CONFIG BAR */}
      <div className="dr-config">
        <div className="dr-config-row">
          <input
            className="dr-topic-input"
            value={topic}
            onChange={e=>setTopic(e.target.value)}
            placeholder="Research topic... e.g. 'quantum computing and cryptography'"
            disabled={running}
            onKeyDown={e=>{if(e.key==="Enter")runResearch();}}
          />
          <div className="dr-config-controls">
            <label className="dr-config-label">ROUNDS</label>
            <input type="number" className="dr-rounds-input" value={rounds} min={1} max={8}
              onChange={e=>setRounds(Math.max(1,Math.min(8,parseInt(e.target.value)||1)))}
              disabled={running}/>
            <label className={`dr-toggle${saveBrain?" on":""}`}>
              <input type="checkbox" checked={saveBrain} onChange={e=>setSaveBrain(e.target.checked)} disabled={running}/>
              SAVE TO BRAIN
            </label>
            {!running
              ? <button className="btn-dr-start" onClick={runResearch} disabled={!topic.trim()||!assign?.model}>
                  ▶ START RESEARCH
                </button>
              : <button className="btn-dr-stop" onClick={()=>setRunning(false)}>■ STOP</button>
            }
          </div>
        </div>
        {!assign?.model && (
          <div className="dr-no-model">⚠ No model set — <button className="cam-link-btn" onClick={goSettings}>open Settings</button></div>
        )}
      </div>

      {/* MAIN BODY */}
      <div className="dr-body">

        {/* COUNCIL PIPELINE */}
        <div className="dr-pipeline">
          <div className="dr-panel-title">COUNCIL PIPELINE</div>

          {/* role cards */}
          {COUNCIL_ROLES.map((role,i)=>{
            const result  = results.find(r=>r.roleId===role.id);
            const isActive = activeRole===role.id;
            const isDone   = !!result;
            return (
              <div key={role.id} className={`dr-role-card ${isActive?"active":""} ${isDone?"done":""}`}>
                <div className="dr-role-top">
                  <span className="dr-role-icon">{role.icon}</span>
                  <div>
                    <div className={`dr-role-name color-${role.color}`}>{role.name}</div>
                    <div className="dr-role-status">
                      {isActive ? <><span className="dr-spinner"/>RUNNING...</>
                       : isDone ? "✓ COMPLETE"
                       : "WAITING"}
                    </div>
                  </div>
                </div>
                {isDone && (
                  <div className="dr-role-summary">{result.content.slice(0,120)}...</div>
                )}
              </div>
            );
          })}

          {/* export */}
          {done && (
            <button className="btn-dr-export" onClick={exportPDF}>
              ↓ EXPORT PDF REPORT
            </button>
          )}

          {/* what's happening */}
          {happening && (
            <div className="dr-happening">
              <div className="dr-happening-label">⚡ {happening.phase}</div>
              <div className="dr-happening-detail">{assign?.model}</div>
              {happening.local && <div className="dr-happening-local">🖥 LOCAL — NO DATA LEAVING</div>}
              {happening.tokens>0 && <div className="dr-happening-tokens">{happening.tokens} tokens</div>}
            </div>
          )}
        </div>

        {/* LIVE OUTPUT */}
        <div className="dr-output" ref={outputRef}>
          <div className="dr-panel-title">
            LIVE OUTPUT
            {activeRole && <span className="dr-output-role">
              {COUNCIL_ROLES.find(r=>r.id===activeRole)?.icon} {COUNCIL_ROLES.find(r=>r.id===activeRole)?.name}
            </span>}
          </div>

          {results.length===0 && !liveText && !running && (
            <div className="dr-output-empty">
              <div className="dr-output-empty-icon">🔬</div>
              <div>Enter a topic and hit START RESEARCH.</div>
              <div className="dr-output-empty-sub">
                The council will run {rounds} round{rounds!==1?"s":""} of analysis,
                each with a different perspective: Researcher → Critic → Synthesizer
                {saveBrain?" → Archivist (saved to Brain)":""}.
              </div>
            </div>
          )}

          {results.map((r,i)=>{
            const role = COUNCIL_ROLES.find(c=>c.id===r.roleId);
            return (
              <div key={i} className="dr-result-block">
                <div className={`dr-result-header color-${role?.color||"green"}`}>
                  {role?.icon} ROUND {i+1} — {role?.name}
                </div>
                <div className="dr-result-content">{r.content}</div>
              </div>
            );
          })}

          {liveText && (
            <div className="dr-result-block live">
              <div className={`dr-result-header color-${COUNCIL_ROLES.find(r=>r.id===activeRole)?.color||"yellow"}`}>
                {COUNCIL_ROLES.find(r=>r.id===activeRole)?.icon} ROUND {activeRound} — {COUNCIL_ROLES.find(r=>r.id===activeRole)?.name}
                <span className="dr-live-badge">LIVE</span>
              </div>
              <div className="dr-result-content">{liveText}<span className="qs-cursor">▌</span></div>
            </div>
          )}
        </div>

      </div>
    </div>
  );
}

