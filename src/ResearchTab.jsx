// QUICK STUDY
// ═══════════════════════════════════════════════════════════════════
function QuickStudy({ conn, assign, goSettings }) {
  const [messages, setMessages] = useState([
    { role:"ai", text:"Research mode active. Ask me anything — I'll go deep.", tokens:0 }
  ]);
  const [input,      setInput]      = useState("");
  const [streaming,  setStreaming]   = useState(false);
  const [happening,  setHappening]  = useState(null);
  const [tokenCount, setTokenCount] = useState(0);
  const [saveQueue,  setSaveQueue]  = useState([]);
  const endRef    = useRef(null);
  const abortRef  = useRef(null);

  useEffect(()=>{ endRef.current?.scrollIntoView({behavior:"smooth"}); },[messages]);

  const send = async () => {
    const text = input.trim();
    if (!text || streaming) return;
    setInput("");
    setMessages(m=>[...m,{role:"user",text}]);
    setStreaming(true);
    setHappening({ phase:"thinking", model:assign?.model, local:conn?.type==="local" });

    if (!conn || !assign?.model) {
      setMessages(m=>[...m,{role:"ai",text:"⚠ No model configured. Open Settings → Section Defaults."}]);
      setStreaming(false); return;
    }

    const history = messages.slice(-8).map(m=>({ role:m.role==="user"?"user":"assistant", content:m.text }));

    try {
      const endpoint = conn.type==="local"
        ? `${conn.url}/v1/chat/completions`
        : `${conn.url}/chat/completions`;

      abortRef.current = new AbortController();
      const res = await fetch(endpoint, {
        method:"POST", signal: abortRef.current.signal,
        headers:{ "Content-Type":"application/json", ...(conn.apiKey?{"Authorization":`Bearer ${conn.apiKey}`}:{}) },
        body: JSON.stringify({
          model: assign.model,
          messages:[
            { role:"system", content:QUICK_STUDY_PROMPT },
            ...history,
            { role:"user", content:text }
          ],
          stream:true, temperature:0.7,
        }),
      });

      if (!res.ok) throw new Error(`HTTP ${res.status}`);

      // streaming response
      setMessages(m=>[...m,{role:"ai",text:"",streaming:true}]);
      setHappening(h=>({...h, phase:"generating"}));
      const reader  = res.body.getReader();
      const decoder = new TextDecoder();
      let full = "", tokens = 0;

      while (true) {
        const {done,value} = await reader.read();
        if (done) break;
        const chunk = decoder.decode(value);
        const lines = chunk.split("\n").filter(l=>l.startsWith("data: "));
        for (const line of lines) {
          const data = line.slice(6);
          if (data==="[DONE]") continue;
          try {
            const j = JSON.parse(data);
            const delta = j.choices?.[0]?.delta?.content || "";
            full += delta;
            tokens++;
            setTokenCount(t=>t+1);
            setMessages(m=>[...m.slice(0,-1),{role:"ai",text:full,streaming:true}]);
            setHappening(h=>({...h,tokens:tokens}));
          } catch(e){}
        }
      }
      setMessages(m=>[...m.slice(0,-1),{role:"ai",text:full,streaming:false,saveable:true}]);
      setHappening(h=>({...h,phase:"done",tokens}));
    } catch(e) {
      if (e.name!=="AbortError")
        setMessages(m=>[...m,{role:"ai",text:`✗ ${e.message}`}]);
    }
    setStreaming(false);
  };

  const saveToBrain = (text) => {
    const entry = { id:Date.now(), text, source:"quick-study", savedAt:new Date().toISOString() };
    const existing = JSON.parse(localStorage.getItem("oni_brain")||"[]");
    localStorage.setItem("oni_brain", JSON.stringify([...existing, entry]));
    setSaveQueue(q=>[...q, entry.id]);
    setTimeout(()=>setSaveQueue(q=>q.filter(i=>i!==entry.id)), 2000);
  };

  return (
    <div className="qs-layout">
      {/* CHAT */}
      <div className="qs-chat">
        <div className="qs-messages">
          {messages.map((m,i)=>(
            <div key={i} className={`qs-msg qs-msg-${m.role}`}>
              <div className="qs-msg-sender">{m.role==="ai"?"ONI RESEARCH":"YOU"}</div>
              <div className="qs-msg-text">{m.text}{m.streaming&&<span className="qs-cursor">▌</span>}</div>
              {m.saveable && !m.saved && (
                <button className="btn-save-brain" onClick={()=>{saveToBrain(m.text);m.saved=true;}}>
                  🧠 SAVE TO BRAIN
                </button>
              )}
            </div>
          ))}
          <div ref={endRef}/>
        </div>
        <div className="qs-input-row">
          <textarea
            className="qs-input"
            value={input}
            onChange={e=>setInput(e.target.value)}
            onKeyDown={e=>{if(e.key==="Enter"&&!e.shiftKey){e.preventDefault();send();}}}
            placeholder={assign?.model ? "Ask anything to study..." : "Configure a model in Settings first..."}
            disabled={streaming}
          />
          <button className="btn-qs-send" onClick={send} disabled={streaming||!input.trim()}>
            {streaming ? "▌" : "SEND ▶"}
          </button>
        </div>
      </div>

      {/* WHAT'S HAPPENING */}
      <div className="qs-happening">
        <div className="qs-happening-title">⚡ WHAT'S HAPPENING</div>
        {!assign?.model ? (
          <div className="qs-happening-body">
            <div className="whats-item warn">No model configured</div>
            <button className="btn-configure-model" onClick={goSettings}>⚙ OPEN SETTINGS</button>
          </div>
        ) : (
          <div className="qs-happening-body">
            <div className="whats-item">
              <span className="whats-label">MODEL</span>
              <span className="whats-val yellow">{assign.model}</span>
            </div>
            <div className="whats-item">
              <span className="whats-label">RUNNING</span>
              <span className={`whats-val ${conn?.type==="local"?"green":"pink"}`}>
                {conn?.type==="local" ? "🖥 LOCAL — YOUR MACHINE" : "☁ CLOUD API"}
              </span>
            </div>
            {conn?.type==="local" && (
              <div className="whats-item">
                <span className="whats-label">DATA LEAVING</span>
                <span className="whats-val green">NONE ✓</span>
              </div>
            )}
            <div className="whats-item">
              <span className="whats-label">STATUS</span>
              <span className="whats-val">{happening?.phase?.toUpperCase()||"IDLE"}</span>
            </div>
            {happening?.tokens > 0 && (
              <div className="whats-item">
                <span className="whats-label">TOKENS</span>
                <span className="whats-val">{tokenCount}</span>
              </div>
            )}
            <div className="whats-divider"/>
            <div className="whats-explainer">
              <div className="whats-explainer-title">HOW IT WORKS</div>
              <p>Your question is tokenized — split into word fragments — then passed to the model running {conn?.type==="local"?"on your CPU/GPU":"via API"}.</p>
              <p>The model predicts the next token one at a time based on patterns learned during training. That's the blinking cursor you see — each flash is a new token being generated.</p>
              {conn?.type==="local" && <p className="whats-highlight">No company sees your questions. This is yours.</p>}
            </div>
          </div>
        )}
      </div>
    </div>
  );
}

