// ─── PDF GENERATION ───────────────────────────────────────────────────
async function generatePDF(topic, rounds, modelName) {
  const { jsPDF } = await import("jspdf");
  const doc = new jsPDF({ orientation:"portrait", unit:"mm", format:"a4" });
  const margin = 20;
  const pageW  = doc.internal.pageSize.getWidth();
  const pageH  = doc.internal.pageSize.getHeight();
  let y = margin;

  const addText = (text, size=10, bold=false, color=[0,0,0]) => {
    doc.setFontSize(size);
    doc.setFont("helvetica", bold ? "bold" : "normal");
    doc.setTextColor(...color);
    const lines = doc.splitTextToSize(text, pageW - margin*2);
    lines.forEach(line => {
      if (y > pageH - margin) { doc.addPage(); y = margin; }
      doc.text(line, margin, y);
      y += size * 0.45;
    });
    y += 3;
  };

  const addDivider = () => {
    doc.setDrawColor(57,255,20);
    doc.setLineWidth(0.3);
    doc.line(margin, y, pageW-margin, y);
    y += 5;
  };

  // Header
  doc.setFillColor(5,5,5);
  doc.rect(0,0,pageW,30,"F");
  addText("ONI // RESEARCH REPORT", 18, true, [57,255,20]);
  addText(`Topic: ${topic}`, 12, false, [200,200,200]);
  addText(`Model: ${modelName}  •  Generated: ${new Date().toLocaleString()}`, 9, false, [120,120,120]);
  addDivider();

  rounds.forEach((round, i) => {
    const role = COUNCIL_ROLES.find(r=>r.id===round.roleId);
    addText(`ROUND ${i+1} — ${role?.name || round.roleId}`, 13, true, [232,255,0]);
    addText(round.content, 10, false, [40,40,40]);
    addDivider();
  });

  addText("Generated by ONI AI Operating System", 8, false, [150,150,150]);
  addText("Running locally — no data sent to external servers", 8, false, [150,150,150]);

  doc.save(`ONI_Research_${topic.replace(/\s+/g,"_").slice(0,30)}_${Date.now()}.pdf`);
}

